package com.vip.CarLoanRegistation.app.model;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;
@Data
@Entity
@AllArgsConstructor
@NoArgsConstructor
public class SelfEmployee {
	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	private Integer selfempId;
	private String selfempName;
	private byte[] selfempBlankcheque;
	private byte[] selfempBankstatement;
	private byte[] form16;
	private String selfempCompanyName;
	private Integer selfempAge;
	private byte[] selfempPhoto;
	private byte[] selfempSignature;
	private byte[] selfempDrivinglicence;
	private String selfempBusinessname;
}
